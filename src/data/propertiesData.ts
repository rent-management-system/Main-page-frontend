// Function to shuffle an array
const shuffleArray = <T>(array: T[]): T[] => {
  const newArray = [...array];
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
  }
  return newArray;
};

// Function to get 1 random apartment, 1 random condominium, and 1 random house
export const getRandomFeaturedProperties = (): Property[] => {
  const apartments = propertiesData.filter((p) => p.type === 'apartment');
  const condominiums = propertiesData.filter((p) => p.type === 'condominium');
  const houses = propertiesData.filter((p) => p.type === 'house');

  const randomApartment = apartments[Math.floor(Math.random() * apartments.length)];
  const randomCondominium = condominiums[Math.floor(Math.random() * condominiums.length)];
  const randomHouse = houses[Math.floor(Math.random() * houses.length)];

  return [randomApartment, randomCondominium, randomHouse];
};

// Function to get 4 random apartments, 4 random condominiums, and 4 random houses
export const getRandomAllProperties = (): Property[] => {
  const apartments = propertiesData.filter((p) => p.type === 'apartment');
  const condominiums = propertiesData.filter((p) => p.type === 'condominium');
  const houses = propertiesData.filter((p) => p.type === 'house');

  const randomApartments = shuffleArray(apartments).slice(0, 4);
  const randomCondominiums = shuffleArray(condominiums).slice(0, 4);
  const randomHouses = shuffleArray(houses).slice(0, 4);

  return [...randomApartments, ...randomCondominiums, ...randomHouses];
};